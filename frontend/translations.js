function t(key) {
  const lang = localStorage.getItem('kzleap_lang') || 'en';
  return (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) || TRANSLATIONS.en[key] || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key  = el.getAttribute('data-i18n');
    const attr = el.getAttribute('data-i18n-attr');
    if (attr) { el.setAttribute(attr, t(key)); }
    else { el.textContent = t(key); }
  });
  const lang = localStorage.getItem('kzleap_lang') || 'en';
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

function setLang(lang) {
  localStorage.setItem('kzleap_lang', lang);
  applyTranslations();
  // Re-run page-specific render functions after language switch
  if (typeof calculate === 'function') {
    calculate();
  }
  if (typeof renderAll === 'function') {
    renderAll();
  }
  if (typeof renderNDCGauges === 'function' && typeof CFG !== 'undefined') {
    renderNDCGauges(CFG, null, CFG.base_co2);
  }
}